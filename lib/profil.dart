                    const SizedBox(height: 16),
                    Row(
                      mainAxisAlignment: MainAxisAlignment.spaceAround,
                      children: [
                        _buildStatItem('Visited', '12', Icons.location_on),
                        _buildStatItem('Planned', '5', Icons.event),
